            "The model also provides a clean launching point for more advanced work. Frame-aware disambiguation, calibrated abstention, and hybrid classical-LLM reasoning can all be layered onto the existing interfaces without discarding the baseline's reproducibility. This makes the system a productive reference architecture for future SRL-QA experimentation.",
