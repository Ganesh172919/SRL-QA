def export_implementation_bundle(config: ProjectConfig) -> Path:
    """Create the required implementation_code.py bundle."""

    module_paths = [
        config.paths.project_root / "config.py",
        config.paths.project_root / "data_loader.py",
        config.paths.project_root / "model.py",
        config.paths.project_root / "trainer.py",
        config.paths.project_root / "evaluator.py",
        config.paths.project_root / "qa_inference.py",
        config.paths.project_root / "hybrid_qa.py",
        config.paths.project_root / "benchmark.py",
        config.paths.project_root / "app.py",
        config.paths.project_root / "pdf_generator.py",
        config.paths.project_root / "main.py",
    ]
    output_path = config.paths.outputs_dir / "implementation_code.py"
    lines: List[str] = [
        '"""Concatenated implementation bundle for the SRL-QA project.',
        "",
        "This file is GENERATED by `pdf_generator.export_implementation_bundle(...)`.",
        "Do not edit it by hand; edit the real modules and regenerate instead.",
        "",
        "Regenerate via:",
        "    python main.py --mode full",
        '"""',
        "",
        "from __future__ import annotations",
        "",
    ]
    for module_path in module_paths:
        if not module_path.exists():
            continue
        lines.append(f"# {'=' * 78}")
        lines.append(f"# MODULE: {module_path.name}")
        lines.append(f"# {'=' * 78}")
        module_lines = module_path.read_text(encoding="utf-8").splitlines()
        # The bundle is a single file, so future-imports can only appear once at the top.
        module_lines = [
            line
            for line in module_lines
            if line.strip() != "from __future__ import annotations"
        ]
        lines.extend(module_lines)
        lines.append("")
    output_path.write_text("\n".join(lines), encoding="utf-8")
    return output_path


def generate_all_pdfs(
    config: ProjectConfig,
    data_stats: Dict[str, Any],
    metrics: Dict[str, Any],
) -> Dict[str, int]:
    """Generate all academic PDF deliverables and the code bundle."""

    assets = create_diagram_assets(config)
    page_counts = {
        "survey.pdf": generate_survey_pdf(config, data_stats, metrics, assets),
        "analysis.pdf": generate_analysis_pdf(config, data_stats, metrics),
        "innovation.pdf": generate_innovation_pdf(config, data_stats, metrics, assets),
        "research_paper.pdf": generate_research_paper_pdf(config, data_stats, metrics, assets),
    }
    export_implementation_bundle(config)
    return page_counts

