def create_frame_graph_diagram(output_path: Path) -> Path:
    """Create a frame-graph illustration for multi-sentence reasoning."""

    output_path.parent.mkdir(parents=True, exist_ok=True)
    figure, axis = plt.subplots(figsize=(8, 5))
    axis.axis("off")
    node_positions = {
        "announce": (1.5, 3.5),
        "cut": (4.0, 3.5),
        "respond": (2.8, 1.5),
    }
    edges = [("announce", "cut"), ("announce", "respond"), ("cut", "respond")]
    for source, target in edges:
        x1, y1 = node_positions[source]
        x2, y2 = node_positions[target]
        axis.annotate(
            "",
            xy=(x2, y2),
            xytext=(x1, y1),
            arrowprops=dict(arrowstyle="->", lw=1.4, color="#284B63"),
        )
    for label, (x, y) in node_positions.items():
        circle = patches.Circle((x, y), radius=0.45, facecolor="#D9E2F3", edgecolor="#284B63", lw=1.4)
        axis.add_patch(circle)
        axis.text(x, y, label, ha="center", va="center", fontsize=10)
    axis.text(2.4, 4.1, "shared entity: company", fontsize=9)
    axis.text(3.1, 2.5, "causal bridge", fontsize=9)
    axis.set_xlim(0, 5.5)
    axis.set_ylim(0.5, 4.6)
    axis.set_title("Semantic Frame Chaining Across Sentences", fontsize=12, fontweight="bold")
    figure.tight_layout()
    figure.savefig(output_path, dpi=300)
    plt.close(figure)
    return output_path


