class AcademicDocTemplate(BaseDocTemplate):
    """Single-column academic document with table-of-contents support."""

    def __init__(self, filename: str, title: str, **kwargs: Any) -> None:
        super().__init__(filename, **kwargs)
        self.title = title
        self._bookmark_counter = 0
        frame = Frame(
            self.leftMargin,
            self.bottomMargin,
            self.width,
            self.height,
            id="normal",
        )
        self.addPageTemplates(
            [
                PageTemplate(
                    id="Main",
                    frames=[frame],
                    onPage=self._draw_header_footer,
                )
            ]
        )

    def afterFlowable(self, flowable: Any) -> None:
        """Register headings for TOC and PDF bookmarks."""

        if hasattr(flowable, "_outline_level"):
            level = getattr(flowable, "_outline_level")
            text = flowable.getPlainText()
            self.notify("TOCEntry", (level, text, self.page))

    def _draw_header_footer(self, canvas: Any, doc: BaseDocTemplate) -> None:
        """Draw a consistent header and footer."""

        canvas.saveState()
        canvas.setFont("Times-Roman", 9)
        canvas.drawString(doc.leftMargin, A4[1] - 1.2 * cm, self.title)
        canvas.drawRightString(
            A4[0] - doc.rightMargin,
            1.0 * cm,
            f"Page {canvas.getPageNumber()}",
        )
        canvas.restoreState()


